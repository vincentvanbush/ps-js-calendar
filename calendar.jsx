var docRef = app.activeDocument;
var month = parseInt(prompt("Podaj miesiąc:", "", "Miesiąc"));
var year = parseInt(prompt("Podaj rok:", "", "Rok"));

var firstDayOfMonth = new Date(year, month - 1, 1).getDay();
if (firstDayOfMonth == 0) firstDayOfMonth = 7;

var numberOfDays = function(month) {
	if ([1, 3, 5, 7, 8, 10, 12].indexOf(month) != -1)
		return 31;
	else if ([4, 6, 9, 11].indexOf(month) != -1)
		return 30;
	else if (month == 2)
		return 28;
}

var nextMonth = function(month) {
	if (month == 12) return 1;
	return month + 1;
}

var previousMonth = function(month) {
	if (month == 1) return 12;
	return month - 1;
}


var dayNumbersSetRef = docRef.layerSets.getByName("dayNumbers");
var dayRowsRef = dayNumbersSetRef.layerSets;


var nameLabelsRef = docRef.layerSets.getByName("nameLabels").layerSets;

dayCells = [];
nameCells = [];

for (i = 1; i <= 6; i++) {
	var rowName = 'row' + i;
	var rowRef = dayRowsRef.getByName(rowName);
	var nameRowRef = nameLabelsRef.getByName(rowName);
	for (j = 1; j <= 7; j++) {
		var cellRef = rowRef.artLayers.getByName(j);
		dayCells.push(cellRef.textItem); // push the text item - just change contents then

		var nameCellRef = nameRowRef.artLayers.getByName(j);
		nameCells.push(nameCellRef.textItem);
	}
}



var num = 1;
grayColor = new SolidColor();
grayColor.rgb.red = 150;
grayColor.rgb.green = 150;
grayColor.rgb.blue = 150;

blackColor = new SolidColor();
blackColor.rgb.red = 0;
blackColor.rgb.green = 0;
blackColor.rgb.blue = 0;

// before the 1st day
var boundary = firstDayOfMonth - 1;
for (i = boundary - 1; i >= 0; i--) {
	dayCells[i].size = 28;
	dayCells[i].justification = Justification.RIGHT;
	prevNum = numberOfDays(previousMonth(month));
	day = prevNum - boundary + 1 + i;
	dayCells[i].color = grayColor;
	dayCells[i].contents = day;
	nameCells[i].color = grayColor;
	nameCells[i].contents = dayNames[previousMonth(month)][day-1];
}

// from the 1st through the last day and after
for (i = boundary; i < dayCells.length; i++) {
	dayCells[i].size = 28;
	dayCells[i].justification = Justification.RIGHT;
	if (num <= numberOfDays(month)) {
		dayCells[i].contents = num;
		dayCells[i].color = blackColor;
		// alert(num-1 + '\n' + dayNames[month][(num)-1]);
		nameCells[i].contents = dayNames[month][(num++)-1];
		nameCells[i].color = blackColor;
	}
	else {
		next = nextMonth(month);
		day = num++ % numberOfDays(month);
		dayCells[i].contents = day;
		dayCells[i].color = grayColor;
		nameCells[i].contents = dayNames[next][day-1];
		nameCells[i].color = grayColor;
	}
}